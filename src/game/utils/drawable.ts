import * as p5 from "p5";

/**
 * Represents an object which can be drawn to the canvas.
 */
export interface Drawable {
    /**
     * Draws the object onto the canvas. Should be called once per frame after
     * calling {@link update}.
     *
     * @param _p - p5 instance.
     */
    draw(_p: p5): void;
}
